public class comparacion {
    
    public static String mayor_menor (double a, double b){
       
        if (a>b){
            return "a es mayor que b";
        }
        else if (a<b){
            return "a es menor que b";    
        }
        else{
            return "a es igual a b";
        }
            
    }    
}        
        
    

